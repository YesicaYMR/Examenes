    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String dni = txtDni.getText();
        String nombre = txtNombre.getText();
        if (nombre.isBlank() || dni.isBlank()) {
            JOptionPane.showMessageDialog(this, "Rellene todos los datos", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (operaciones.existeDni(dni)) {
                JOptionPane.showMessageDialog(this, "Ya existe un usuario con este dni", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                operaciones.crearProfesor(dni, nombre);
                JOptionPane.showMessageDialog(this, "Profesor dado de alta", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            }
        }

    }//GEN-LAST:event_btnAceptarActionPerformed

